/*
Why OOPS?


-> C++ language was designed with the main intention of adding object-oriented features to C language.

-> As the size of the program increases, readability, maintainability and bug-free nature of programs decreases.

-> This was the major problem with languages like C which relied upon functions or procedures (hence the name procedural oriented programming laguage)

->As a result, the possibility of not addressing the problem in an effective manner was high.

-> Also, as data was almost negelected, data security was wasily compromised.

-> Using classes solves this problem by modelling program as a real world scenario.
*/

